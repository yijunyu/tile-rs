//! MLIR-to-IRON-Python translator for AMD AIE targets (NPUeval-compatible).
//!
//! Converts merged MLIR modules (LLVM dialect with `ascend_tile_*` intrinsics)
//! into IRON Python source (the MLIR-AIE high-level Python DSL), which can be
//! compiled by `aiecc.py` and simulated on adablue without any hardware.
//!
//! # IRON Python Output Format
//!
//! A typical generated kernel looks like:
//!
//! ```python
//! import math
//! import numpy as np
//! from aie.iron import ObjectFifo, Program, Runtime, Worker
//! from aie.iron.placers import SequentialPlacer
//! from aie.iron.device import NPU2Col1
//! from aie.iron.controlflow import range_
//!
//! TILE_WIDTH   = 32
//! PROBLEM_SIZE = 1024
//!
//! def tile_softmax():
//!     tile_ty = np.ndarray[(TILE_WIDTH,),   np.dtype[np.float32]]
//!     mem_ty  = np.ndarray[(TILE_WIDTH*2,), np.dtype[np.float32]]
//!     all_ty  = np.ndarray[(PROBLEM_SIZE,), np.dtype[np.float32]]
//!
//!     of_in0  = ObjectFifo(mem_ty,  name="in0")
//!     of_in1  = of_in0.cons().forward(obj_type=tile_ty)
//!     of_out0 = ObjectFifo(tile_ty, name="out0")
//!     of_out1 = of_out0.cons().forward(obj_type=mem_ty)
//!
//!     def core_body(of_in1, of_out0):
//!         elem_in  = of_in1.acquire(1)
//!         elem_out = of_out0.acquire(1)
//!         # step 1: row max
//!         _max = elem_in[0]
//!         for i in range_(TILE_WIDTH):
//!             if elem_in[i] > _max:
//!                 _max = elem_in[i]
//!         # step 2+3: exp(x - max)
//!         for i in range_(TILE_WIDTH):
//!             elem_out[i] = math.exp(elem_in[i] - _max)
//!         # step 4: sum
//!         _sum = 0.0
//!         for i in range_(TILE_WIDTH):
//!             _sum = _sum + elem_out[i]
//!         # step 5: divide
//!         for i in range_(TILE_WIDTH):
//!             elem_out[i] = elem_out[i] / _sum
//!         of_in1.release(1)
//!         of_out0.release(1)
//!
//!     worker = Worker(core_body, fn_args=[of_in1.cons(), of_out0.prod()])
//!     rt = Runtime()
//!     with rt.sequence(all_ty, all_ty) as (inT, outT):
//!         rt.start(worker)
//!         rt.fill(of_in0.prod(),   inT)
//!         rt.drain(of_out1.cons(), outT, wait=True)
//!     return Program(NPU2Col1(), rt).resolve_program(SequentialPlacer())
//!
//! module = tile_softmax()
//! res = module.operation.verify()
//! if res == True:
//!     print(module)
//! else:
//!     print(res)
//! ```
//!
//! # Mapping from tile_std tile intrinsics to IRON patterns
//!
//! | tile_std intrinsic | IRON core_body pattern |
//! |---|---|
//! | `ascend_tile_load_f32` | ObjectFifo acquire (implicit load via DMA) |
//! | `ascend_tile_store_f32` | ObjectFifo release (implicit store via DMA) |
//! | `ascend_tile_add_f32(_, a, b, R, C)` | `for i in range_(C): out[i] = a[i] + b[i]` |
//! | `ascend_tile_sub_f32(_, a, b, R, C)` | `for i in range_(C): out[i] = a[i] - b[i]` |
//! | `ascend_tile_mul_f32(_, a, b, R, C)` | `for i in range_(C): out[i] = a[i] * b[i]` |
//! | `ascend_tile_exp_f32(_, src, R, C)` | `for i in range_(C): out[i] = math.exp(src[i])` |
//! | `ascend_tile_softmax_f32(_, src, R, C)` | 5-loop decomposition (max→sub+exp→sum→div) |
//! | `ascend_tile_matmul_f32(_, a, b, M, K, N)` | nested range_ loops with accumulator |
//! | `ascend_tile_reduce_max_f32(_, src, R, C)` | scalar reduce loop → scalar result |
//! | `ascend_tile_reduce_sum_f32(_, src, R, C)` | scalar reduce loop → scalar result |
//! | `ascend_tile_scale_f32(_, src, s, R, C)` | `for i in range_(C): out[i] = src[i] * s` |
//! | `ascend_tile_rope_f32(_, src, pos, R, C)` | pair-wise rotary embedding by position angle |
//!
//! # Simulation (no hardware)
//!
//! ```bash
//! # Compile IRON Python → MLIR-AIE module text
//! python3 kernel.aie.py npu2 > design.mlir
//!
//! # Compile + generate simulator
//! aiecc.py --aiesim design.mlir
//!
//! # Build and run simulator
//! make -C design.mlir.prj/sim
//! ./design.mlir.prj/sim/aiesimulator
//! ```

use std::collections::HashMap;
use std::fmt::Write;

use crate::mlir_to_pto::{
    extract_call_args, extract_result_ssa, infer_dtype_from_name, is_builtin_helper,
    parse_const_arg, parse_module, FuncArg, MlirFunc,
};

/// Convert MLIR text (merged module, LLVM dialect) into IRON Python source
/// consumable by `python3 <file>.aie.py npu2 | aiecc.py --aiesim`.
pub fn convert_mlir_to_aie(mlir_text: &str) -> Result<String, String> {
    let module = parse_module(mlir_text)?;

    let mut out = String::with_capacity(4096);
    writeln!(out, "# Generated by tile-rs mlir_to_aie — DO NOT EDIT").unwrap();
    writeln!(out, "# Compile: python3 <this_file> npu2 > design.mlir").unwrap();
    writeln!(out, "# Simulate: aiecc.py --aiesim design.mlir").unwrap();
    writeln!(out).unwrap();

    let mut kernel_count = 0;
    for func in &module.functions {
        if func.is_entry && !func.body_lines.is_empty() && !is_builtin_helper(&func.name) {
            generate_func_aie(func, &mut out)?;
            kernel_count += 1;
        }
    }

    if kernel_count == 0 {
        return Err("No entry-point kernel functions found in MLIR module".into());
    }

    Ok(out)
}

// ---------------------------------------------------------------------------
// IRON function generator
// ---------------------------------------------------------------------------

fn generate_func_aie(func: &MlirFunc, out: &mut String) -> Result<(), String> {
    let mut ctx = AieContext::new();

    // Build clean Python param names: %arg0 → p0, p1, ...
    let ptr_args: Vec<&FuncArg> = func.args.iter().filter(|a| a.is_gm).collect();
    for (i, arg) in ptr_args.iter().enumerate() {
        let py_name = format!("p{}", i);
        ctx.ptr_aliases.insert(arg.name.clone(), py_name.clone());
        let dtype = infer_dtype_from_name(&arg.name);
        ctx.arg_dtypes.insert(arg.name.clone(), dtype.to_string());
        ctx.arg_dtypes.insert(py_name.clone(), dtype.to_string());
    }

    // First pass: collect ops and determine tile dimensions
    let body_ops = collect_ops(&func.body_lines, &mut ctx)?;

    // Determine TILE_WIDTH and PROBLEM_SIZE from collected shapes
    let tile_width = ctx.tile_width.unwrap_or(32);
    let problem_size = ctx.problem_size.unwrap_or(tile_width);
    // n_tiles: how many tiles fit in problem_size.  Single-tile kernels → 1.
    // MemTile ping-pong buffer holds n_tiles * TILE_WIDTH elements so that the
    // MemTile can stage a full problem's worth of data while the ComputeTile
    // processes individual tiles.  For single-tile kernels this collapses to
    // TILE_WIDTH (no ping-pong needed, but we use 2 as minimum so IRON can
    // double-buffer between MemTile and the shim DMA).
    let n_tiles = if problem_size > tile_width { problem_size / tile_width } else { 1 };
    let mem_width = tile_width * n_tiles.max(2);
    let dtype_str = ctx.primary_dtype.as_deref().unwrap_or("f32");
    let np_dtype = np_dtype_str(dtype_str);

    // Determine how many distinct input/output GM ptrs appear
    let n_inputs = ctx.input_ptrs.len();
    let n_outputs = ctx.output_ptrs.len();

    // ── Emit imports ──────────────────────────────────────────────────────────
    writeln!(out, "import sys").unwrap();
    writeln!(out, "import numpy as np").unwrap();
    writeln!(out, "from aie.iron import ObjectFifo, Program, Runtime, Worker").unwrap();
    writeln!(out, "from aie.iron.placers import SequentialPlacer").unwrap();
    writeln!(out, "from aie.iron.device import NPU1Col1, NPU2Col1").unwrap();
    writeln!(out, "from aie.iron.controlflow import range_").unwrap();
    writeln!(out, "from aie.dialects import math as aie_math").unwrap();
    writeln!(out, "from aie.dialects.arith import MaxNumFOp, AddFOp").unwrap();
    writeln!(out, "from aie.dialects.scf import yield_").unwrap();
    writeln!(out).unwrap();
    writeln!(out, "TILE_WIDTH   = {}", tile_width).unwrap();
    // PROBLEM_SIZE: compile-time default; can be overridden at runtime via
    // sys.argv[2] so the same .aie.py works for multi-tile host invocations.
    // Usage: python3 kernel.aie.py npu2 4096
    writeln!(out, "_PROBLEM_SIZE_DEFAULT = {}", problem_size).unwrap();
    writeln!(
        out,
        "PROBLEM_SIZE = int(sys.argv[2]) if len(sys.argv) > 2 else _PROBLEM_SIZE_DEFAULT"
    )
    .unwrap();
    writeln!(out, "N_TILES      = PROBLEM_SIZE // TILE_WIDTH").unwrap();
    writeln!(out, "MEM_WIDTH    = TILE_WIDTH * max(N_TILES, 2)  # MemTile ping-pong buffer").unwrap();
    writeln!(out).unwrap();

    // Device selection from command-line arg (same as mlir-aie examples)
    writeln!(out, "if len(sys.argv) > 1 and sys.argv[1] == \"npu\":").unwrap();
    writeln!(out, "    _dev = NPU1Col1()").unwrap();
    writeln!(out, "else:").unwrap();
    writeln!(out, "    _dev = NPU2Col1()").unwrap();
    writeln!(out).unwrap();

    // ── Emit kernel function ──────────────────────────────────────────────────
    writeln!(out, "def {}():", func.name).unwrap();
    writeln!(
        out,
        "    tile_ty = np.ndarray[(TILE_WIDTH,), np.dtype[{}]]",
        np_dtype
    )
    .unwrap();
    // mem_ty uses MEM_WIDTH (runtime constant) so multi-tile invocations
    // automatically size the MemTile buffer to hold N_TILES × TILE_WIDTH elements.
    writeln!(
        out,
        "    mem_ty  = np.ndarray[(MEM_WIDTH,), np.dtype[{}]]",
        np_dtype
    )
    .unwrap();
    writeln!(
        out,
        "    all_ty  = np.ndarray[(PROBLEM_SIZE,), np.dtype[{}]]",
        np_dtype
    )
    .unwrap();
    let _ = mem_width; // now emitted as runtime MEM_WIDTH
    writeln!(out).unwrap();

    // Emit ObjectFifos for each input
    for (idx, _) in (0..n_inputs).enumerate() {
        writeln!(
            out,
            "    of_in{idx}a = ObjectFifo(mem_ty,  name=\"in{idx}\")",
            idx = idx
        )
        .unwrap();
        writeln!(
            out,
            "    of_in{idx}b = of_in{idx}a.cons().forward(obj_type=tile_ty)",
            idx = idx
        )
        .unwrap();
    }
    // Emit ObjectFifos for each output
    for (idx, _) in (0..n_outputs).enumerate() {
        writeln!(
            out,
            "    of_out{idx}a = ObjectFifo(tile_ty, name=\"out{idx}\")",
            idx = idx
        )
        .unwrap();
        writeln!(
            out,
            "    of_out{idx}b = of_out{idx}a.cons().forward(obj_type=mem_ty)",
            idx = idx
        )
        .unwrap();
    }
    writeln!(out).unwrap();

    // ── Emit core_body function ───────────────────────────────────────────────
    // Collect fn_args and def signature
    let mut fn_args_def = Vec::new();
    let mut fn_args_call = Vec::new();
    for idx in 0..n_inputs {
        fn_args_def.push(format!("of_in{idx}b", idx = idx));
        fn_args_call.push(format!("of_in{idx}b.cons()", idx = idx));
    }
    for idx in 0..n_outputs {
        fn_args_def.push(format!("of_out{idx}a", idx = idx));
        fn_args_call.push(format!("of_out{idx}a.prod()", idx = idx));
    }

    writeln!(
        out,
        "    def core_body({}):",
        fn_args_def.join(", ")
    )
    .unwrap();
    writeln!(out, "        for _ in range_(sys.maxsize):").unwrap();

    // Acquire all inputs
    for idx in 0..n_inputs {
        writeln!(
            out,
            "            elem_in{idx} = of_in{idx}b.acquire(1)",
            idx = idx
        )
        .unwrap();
    }
    // Acquire all outputs
    for idx in 0..n_outputs {
        writeln!(
            out,
            "            elem_out{idx} = of_out{idx}a.acquire(1)",
            idx = idx
        )
        .unwrap();
    }

    // Emit compute ops
    let primary_in = if n_inputs > 0 { "elem_in0" } else { "elem_in0" };
    let primary_out = if n_outputs > 0 { "elem_out0" } else { "elem_out0" };
    for op in &body_ops {
        emit_aie_op(op, primary_in, primary_out, tile_width, out);
    }

    // Release all
    for idx in 0..n_inputs {
        writeln!(
            out,
            "            of_in{idx}b.release(1)",
            idx = idx
        )
        .unwrap();
    }
    for idx in 0..n_outputs {
        writeln!(
            out,
            "            of_out{idx}a.release(1)",
            idx = idx
        )
        .unwrap();
    }
    writeln!(out).unwrap();

    // ── Emit Worker + Runtime ─────────────────────────────────────────────────
    writeln!(
        out,
        "    worker = Worker(core_body, fn_args=[{}])",
        fn_args_call.join(", ")
    )
    .unwrap();
    writeln!(out).unwrap();
    writeln!(out, "    rt = Runtime()").unwrap();

    // Build sequence signature: all inputs then all outputs
    let seq_params: Vec<String> = (0..n_inputs + n_outputs)
        .map(|i| format!("t{}", i))
        .collect();
    writeln!(
        out,
        "    with rt.sequence({}) as ({}):",
        vec!["all_ty"; n_inputs + n_outputs].join(", "),
        seq_params.join(", ")
    )
    .unwrap();
    writeln!(out, "        rt.start(worker)").unwrap();
    for idx in 0..n_inputs {
        writeln!(
            out,
            "        rt.fill(of_in{idx}a.prod(),    t{t})",
            idx = idx,
            t = idx
        )
        .unwrap();
    }
    for idx in 0..n_outputs {
        writeln!(
            out,
            "        rt.drain(of_out{idx}b.cons(),  t{t}, wait=True)",
            idx = idx,
            t = n_inputs + idx
        )
        .unwrap();
    }
    writeln!(out).unwrap();
    writeln!(
        out,
        "    return Program(_dev, rt).resolve_program(SequentialPlacer())"
    )
    .unwrap();
    writeln!(out).unwrap();

    // ── Emit top-level call ───────────────────────────────────────────────────
    writeln!(out, "module = {}()", func.name).unwrap();
    writeln!(out, "res = module.operation.verify()").unwrap();
    writeln!(out, "if res == True:").unwrap();
    writeln!(out, "    print(module)").unwrap();
    writeln!(out, "else:").unwrap();
    writeln!(out, "    print(res)").unwrap();

    Ok(())
}

