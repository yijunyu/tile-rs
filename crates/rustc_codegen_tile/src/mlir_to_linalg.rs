    // ── Unknown-shape / unknown-tensor error arms ───────────────────────────
    //
    // Each test feeds an `__tile_<op>` call whose consumed operand is a
    // `%ghost` SSA that was never produced by an `__tile_load_*`, so the
    // `.ok_or_else(...)` shape/tensor lookup inside the matching `translate_*`
    // fires. These exercise the error closures that the happy-path tests above
    // never reach. The op `%r`/`%c` operands are real constants; only the tile
    // operand is the dangling `%ghost`.
    //
    // Helper macro: emit a `module { llvm.func @g(...) { ... } }` body whose
    // single intrinsic call is `$call`, where `$call` references `%ghost`.
    macro_rules! ghost_mlir {
        ($call:expr) => {
            concat!(
                "\nmodule {\n",
                "  llvm.func @g(%arg0: !llvm.ptr<1>, %arg1: !llvm.ptr<1>) attributes {hacc.entry} {\n",
                "    ^bb0:\n",
                "    %r = llvm.mlir.constant(1 : i32) : i32\n",
                "    %c = llvm.mlir.constant(64 : i32) : i32\n",
                "    %s = llvm.mlir.constant(1.250000e-01 : f32) : f32\n",
                "    %m = llvm.mlir.constant(0.000000e+00 : f32) : f32\n",
                "    %a = llvm.call @__tile_load_f32(%arg0, %r, %c) : (!llvm.ptr<1>, i32, i32) -> i32\n",
                "    ", $call, "\n",
                "    llvm.call @__tile_store_f32(%arg1, %y, %r, %c) : (!llvm.ptr<1>, i32, i32, i32) -> ()\n",
                "    llvm.return\n",
                "  }\n",
                "}\n",
            )
        };
    }

