/// Opaque single-source unary op carried through the bridge as a
/// pass-through `linalg.generic` with an `__tile_op = "<name>"`
/// attribute. Used for ops that have no meaningful decomposition into
/// standard linalg/arith (rope_inplace, kv_cache_update, etc.).
///
/// The region body yields `%in` unchanged; the semantic meaning lives
/// entirely in the `__tile_op` attribute, which the ingress
/// recognizes to re-emit the original intrinsic. Downstream consumers
/// (vendor backends, Torch-MLIR, IREE) that don't understand the
/// attribute will see a valid identity generic over the tensor.
/// Variant of `translate_opaque_unary` for intrinsics with a compile-time
/// f32 scalar as arg[2] (e.g. quantize/dequantize `scale`). Emits the
/// `scale = <lit> : f32` extra attribute so downstream quant-aware passes
/// can recover the factor.
fn translate_scaled_opaque(
    op_name: &str,
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if args.len() < 5 {
        return Err(format!(
            "__tile_{}: expected 5 args, got {}",
            op_name,
            args.len()
        ));
    }
    let src = args[1].trim().to_string();
    let scale_lit = ctx.resolve_float(args[2].trim());
    translate_opaque_generic(
        op_name,
        &[src.as_str()],
        &[("scale", format!("{} : f32", scale_lit))],
        result,
        ctx,
    )
}

fn translate_opaque_unary(
    op_name: &str,
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if args.len() < 4 {
        return Err(format!(
            "__tile_{}: expected 4 args, got {}",
            op_name,
            args.len()
        ));
    }
    let first = args[1].trim().to_string();
    translate_opaque_generic(op_name, &[first.as_str()], &[], result, ctx)
}

/// Multi-input variant of `translate_opaque_unary`. `src_indices` picks
/// which positional args of the intrinsic are tile SSAs (by convention
/// the first arg is always `dst`, skipped here). All selected sources
/// must share the same shape and dtype — that's enforced by the output
/// generic's identity indexing_maps.
fn translate_opaque_multi(
    op_name: &str,
    args: &[String],
    src_indices: &[usize],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    let max_idx = *src_indices.iter().max().unwrap_or(&0);
    if args.len() <= max_idx {
        return Err(format!(
            "__tile_{}: not enough args (need at least {}, got {})",
            op_name,
            max_idx + 1,
            args.len()
        ));
    }
    let srcs: Vec<String> = src_indices
        .iter()
        .map(|i| args[*i].trim().to_string())
        .collect();
    let src_refs: Vec<&str> = srcs.iter().map(|s| s.as_str()).collect();
    translate_opaque_generic(op_name, &src_refs, &[], result, ctx)
}

/// Emit an opaque pass-through `linalg.generic` with N input tensors
/// sharing the output shape. The `__tile_op = "<name>"` attribute is
/// the only thing that tells downstream consumers what the real op is.
/// The body is `linalg.yield %in0 : f32` regardless of arity — it keeps
/// the IR valid for mlir-opt while the attribute carries the truth.
fn translate_opaque_generic(
    op_name: &str,
    src_ssas: &[&str],
    extra_attrs: &[(&str, String)],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    translate_opaque_generic_shaped(op_name, src_ssas, extra_attrs, None, result, ctx)
}

/// Variant of [`translate_opaque_generic`] that accepts an explicit
/// output shape. When `out_shape_override` is `Some((R, C, dtype))`, the
/// output tensor is emitted with that shape and rank-1 is inferred when
/// `C == 1`. When `None`, behavior is identical to the default helper
/// (output = first-input shape).
///
/// This unlocks bridging of shape-changing opaque intrinsics such as
/// `embedding_f32 (V,D) → (COUNT,D)` and `cross_entropy_f32 (R,C) →
/// (R,1)`. The indexing_maps and basic-block body still assume identity
/// read access on each input — callers that need a true gather shape
/// must provide their own body (see `translate_row_argmax` for the
/// dual-output pattern).
fn translate_opaque_generic_shaped(
    op_name: &str,
    src_ssas: &[&str],
    extra_attrs: &[(&str, String)],
    out_shape_override: Option<(u32, u32, &str)>,
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if src_ssas.is_empty() {
        return Err(format!("{}: no source tensors", op_name));
    }
    let first = src_ssas[0];
    let shape = if let Some((r, c, d)) = out_shape_override {
        (r, c, d.to_string())
    } else {
        ctx.shape_of(first)
            .ok_or_else(|| format!("{}: unknown shape for {}", op_name, first))?
    };

    // Resolve each source SSA to its linalg tensor name.
    let mut src_tensors: Vec<String> = Vec::with_capacity(src_ssas.len());
    for s in src_ssas {
        let t = ctx
            .tensor_of(s)
            .cloned()
            .ok_or_else(|| format!("{}: unknown tensor {}", op_name, s))?;
        src_tensors.push(t);
    }
    let n = src_ssas.len();

    let empty = ctx.fresh();
    let out = ctx.fresh();
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}x{}>",
        empty, shape.0, shape.1, shape.2
    ));

    // Build `indexing_maps = [identity; N+1]` (N inputs + 1 output).
    let identity = "affine_map<(d0, d1) -> (d0, d1)>";
    let maps: Vec<&str> = std::iter::repeat(identity).take(n + 1).collect();
    // Build extra-attr string: caller owns the MLIR-literal form, e.g.
    //   ("pos", "7 : i32")            → ", pos = 7 : i32"
    //   ("scale", "2.500000e-01 : f32") → ", scale = 2.500000e-01 : f32"
    let mut extras = String::new();
    for (k, v) in extra_attrs {
        extras.push_str(&format!(", {} = {}", k, v));
    }
    // Build `ins(%a, %b : tensor<...>, tensor<...>)` and `^bb0(%in0, %in1, %out)`.
    let tensor_ty = format!("tensor<{}x{}x{}>", shape.0, shape.1, shape.2);
    let ins_ssas: String = src_tensors.join(", ");
    let ins_tys: String = std::iter::repeat(tensor_ty.as_str())
        .take(n)
        .collect::<Vec<_>>()
        .join(", ");
    let bb_args: String = (0..n)
        .map(|i| format!("%in{}: {}", i, shape.2))
        .collect::<Vec<_>>()
        .join(", ");

    ctx.body.push(format!(
        "{} = linalg.generic {{indexing_maps = [{}], iterator_types = [\"parallel\", \"parallel\"], __tile_op = \"{}\"{}}} ins({} : {}) outs({} : {}) {{",
        out,
        maps.join(", "),
        op_name,
        extras,
        ins_ssas,
        ins_tys,
        empty,
        tensor_ty,
    ));
    ctx.body
        .push(format!("  ^bb0({}, %out: {}):", bb_args, shape.2));
    ctx.body
        .push(format!("    linalg.yield %in0 : {}", shape.2));
    ctx.body.push(format!("}} -> {}", tensor_ty));

    if let Some(r) = result {
        ctx.record_tensor(&r, &out, shape);
    }
    Ok(())
}

