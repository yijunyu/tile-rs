fn translate_elementwise_binop(
    linalg_op: &str,
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    // Signature: (dst_ignored, a, b, rows, cols)
    if args.len() < 5 {
        return Err(format!(
            "{}: expected 5 args, got {}",
            linalg_op,
            args.len()
        ));
    }
    let a_ssa = args[1].trim();
    let b_ssa = args[2].trim();
    let shape = ctx
        .shape_of(a_ssa)
        .or_else(|| ctx.shape_of(b_ssa))
        .ok_or_else(|| {
            format!(
                "{}: cannot infer shape from operands {} / {}",
                linalg_op, a_ssa, b_ssa
            )
        })?;
    let a_tensor = ctx
        .tensor_of(a_ssa)
        .cloned()
        .ok_or_else(|| format!("{}: unknown tensor {}", linalg_op, a_ssa))?;
    let b_tensor = ctx
        .tensor_of(b_ssa)
        .cloned()
        .ok_or_else(|| format!("{}: unknown tensor {}", linalg_op, b_ssa))?;

    let empty = ctx.fresh();
    let out = ctx.fresh();
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}x{}>",
        empty, shape.0, shape.1, shape.2
    ));
    ctx.body.push(format!(
        "{} = {} ins({}, {} : tensor<{}x{}x{}>, tensor<{}x{}x{}>) outs({} : tensor<{}x{}x{}>) -> tensor<{}x{}x{}>",
        out, linalg_op,
        a_tensor, b_tensor,
        shape.0, shape.1, shape.2,
        shape.0, shape.1, shape.2,
        empty,
        shape.0, shape.1, shape.2,
        shape.0, shape.1, shape.2,
    ));
    if let Some(r) = result {
        ctx.record_tensor(&r, &out, shape);
    }
    Ok(())
}

