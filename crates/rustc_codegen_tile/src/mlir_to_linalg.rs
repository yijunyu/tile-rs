#[derive(Copy, Clone)]
enum ReduceKind {
    Sum,
    Max,
    // Row-wise absmax = max(|x|) — decomposed as linalg.abs into a tmp
    // (R,C) tensor, then reduce maximumf along dim 1.
    AbsMax,
}

fn translate_row_reduce(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
    kind: ReduceKind,
) -> Result<(), String> {
    // Signature: (dst_ignored, src, rows, cols) — reduce along cols (dim 1) → (rows,1)
    if args.len() < 4 {
        return Err(format!(
            "__tile_reduce_*_f32: expected 4 args, got {}",
            args.len()
        ));
    }
    let src_ssa = args[1].trim();
    let rows = ctx.resolve_const(args[2].trim());
    let cols = ctx.resolve_const(args[3].trim());
    let src_tensor = ctx
        .tensor_of(src_ssa)
        .cloned()
        .ok_or_else(|| format!("reduce: unknown tensor {}", src_ssa))?;

    // For absmax: emit linalg.abs into a fresh (R,C) tensor first, then
    // reduce that instead of the original src.
    let reduce_input = if matches!(kind, ReduceKind::AbsMax) {
        let abs_empty = ctx.fresh();
        let abs_out = ctx.fresh();
        ctx.body.push(format!(
            "{} = tensor.empty() : tensor<{}x{}xf32>",
            abs_empty, rows, cols
        ));
        ctx.body.push(format!(
            "{} = linalg.abs ins({} : tensor<{}x{}xf32>) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
            abs_out, src_tensor, rows, cols, abs_empty, rows, cols, rows, cols
        ));
        abs_out
    } else {
        src_tensor
    };
    let (reduce_init, body_op) = match kind {
        ReduceKind::Sum => ("0.000000e+00", "arith.addf"),
        // -inf for max-reduce and absmax (identity for maximumf)
        ReduceKind::Max | ReduceKind::AbsMax => ("0xFF800000", "arith.maximumf"),
    };

    let c_init = ctx.fresh();
    let empty = ctx.fresh();
    let filled = ctx.fresh();
    let out = ctx.fresh();
    ctx.body
        .push(format!("{} = arith.constant {} : f32", c_init, reduce_init));
    ctx.body
        .push(format!("{} = tensor.empty() : tensor<{}xf32>", empty, rows));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : f32) outs({} : tensor<{}xf32>) -> tensor<{}xf32>",
        filled, c_init, empty, rows, rows
    ));
    ctx.body.push(format!(
        "{} = linalg.reduce ins({} : tensor<{}x{}xf32>) outs({} : tensor<{}xf32>) dimensions = [1]",
        out, reduce_input, rows, cols, filled, rows
    ));
    ctx.body.push("  (%in: f32, %acc: f32) {".to_string());
    ctx.body
        .push(format!("    %r = {} %in, %acc : f32", body_op));
    ctx.body.push("    linalg.yield %r : f32".to_string());
    ctx.body.push("  }".to_string());
    if let Some(r) = result {
        // Rank-1 output represented as (rows,1) with rank-1 flag set so the
        // signature prints as `tensor<Rxf32>`.
        ctx.record_tensor(&r, &out, (rows, 1, "f32".to_string()));
        ctx.rank1_tensors.insert(out);
    }
    Ok(())
}

/// Row-wise argmax: `tensor<RxCxf32> → tensor<Rxi32>` via a dual-output
/// `linalg.generic` that threads (running_max, running_idx) through the
/// reduction. Linalg.reduce only supports single-output scalars, so we
/// hand-roll the generic with two outputs and yield the tuple.
fn translate_row_argmax(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
    extra_attrs: &[(&str, String)],
) -> Result<(), String> {
    if args.len() < 4 {
        return Err(format!(
            "__tile_argmax_f32: expected 4 args, got {}",
            args.len()
        ));
    }
    let src_ssa = args[1].trim();
    let rows = ctx.resolve_const(args[2].trim());
    let cols = ctx.resolve_const(args[3].trim());
    let src_tensor = ctx
        .tensor_of(src_ssa)
        .cloned()
        .ok_or_else(|| format!("argmax: unknown tensor {}", src_ssa))?;

    let c_neginf = ctx.fresh();
    let c_zero = ctx.fresh();
    let empty_v = ctx.fresh();
    let empty_i = ctx.fresh();
    let filled_v = ctx.fresh();
    let filled_i = ctx.fresh();
    let out_v = ctx.fresh();
    let out_i = ctx.fresh();

    ctx.body.push(format!(
        "{} = arith.constant 0xFF800000 : f32",
        c_neginf
    ));
    ctx.body.push(format!("{} = arith.constant 0 : i32", c_zero));
    ctx.body
        .push(format!("{} = tensor.empty() : tensor<{}xf32>", empty_v, rows));
    ctx.body
        .push(format!("{} = tensor.empty() : tensor<{}xi32>", empty_i, rows));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : f32) outs({} : tensor<{}xf32>) -> tensor<{}xf32>",
        filled_v, c_neginf, empty_v, rows, rows
    ));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : i32) outs({} : tensor<{}xi32>) -> tensor<{}xi32>",
        filled_i, c_zero, empty_i, rows, rows
    ));
    // Build the optional extra-attr string (e.g. for sample_top_p's
    // hyperparameters). Caller owns MLIR-literal form including type.
    let mut extras = String::new();
    for (k, v) in extra_attrs {
        extras.push_str(&format!(", {} = {}", k, v));
    }
    ctx.body.push(format!(
        "{}:2 = linalg.generic {{indexing_maps = [affine_map<(d0, d1) -> (d0, d1)>, affine_map<(d0, d1) -> (d0)>, affine_map<(d0, d1) -> (d0)>], iterator_types = [\"parallel\", \"reduction\"]{}}} ins({} : tensor<{}x{}xf32>) outs({}, {} : tensor<{}xf32>, tensor<{}xi32>) {{",
        out_v, extras, src_tensor, rows, cols, filled_v, filled_i, rows, rows
    ));
    ctx.body
        .push("  ^bb0(%in: f32, %av: f32, %ai: i32):".to_string());
    ctx.body.push("    %idx = linalg.index 1 : index".to_string());
    ctx.body
        .push("    %idx_i = arith.index_cast %idx : index to i32".to_string());
    ctx.body
        .push("    %gt = arith.cmpf ogt, %in, %av : f32".to_string());
    ctx.body
        .push("    %nv = arith.select %gt, %in, %av : f32".to_string());
    ctx.body
        .push("    %ni = arith.select %gt, %idx_i, %ai : i32".to_string());
    ctx.body
        .push("    linalg.yield %nv, %ni : f32, i32".to_string());
    ctx.body.push(format!(
        "  }} -> (tensor<{}xf32>, tensor<{}xi32>)",
        rows, rows
    ));

    // The i32 indices are projection #1 of the dual-output generic.
    // record_tensor maps the llvm-dialect result SSA to the projected
    // tensor name used in subsequent ops (store).
    if let Some(r) = result {
        let projected = format!("{}#1", out_v);
        ctx.record_tensor(&r, &projected, (rows, 1, "i32".to_string()));
        ctx.rank1_tensors.insert(projected);
    }
    let _ = out_i; // silence unused warning — kept to preserve SSA freshness
    Ok(())
}

fn translate_rms_norm(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    // Signature: (dst, src, eps, rows, cols)
    if args.len() < 5 {
        return Err(format!(
            "__tile_rms_norm_f32: expected 5 args, got {}",
            args.len()
        ));
    }
    let src_ssa = args[1].trim();
    let eps_str = ctx.resolve_float(args[2].trim());
    let shape = ctx
        .shape_of(src_ssa)
        .ok_or_else(|| format!("rms_norm: cannot infer shape from {}", src_ssa))?;
    let src_tensor = ctx
        .tensor_of(src_ssa)
        .cloned()
        .ok_or_else(|| format!("rms_norm: unknown tensor {}", src_ssa))?;
    let (r, c, d) = shape.clone();

    let c0 = ctx.fresh();
    let cn = ctx.fresh();
    let ceps = ctx.fresh();
    let sq_init = ctx.fresh();
    let sq = ctx.fresh();
    let red_init = ctx.fresh();
    let red_fill = ctx.fresh();
    let red = ctx.fresh();
    let scale_init = ctx.fresh();
    let scale = ctx.fresh();
    let out_init = ctx.fresh();
    let out = ctx.fresh();

    ctx.body.push(format!("{} = arith.constant 0.000000e+00 : {}", c0, d));
    ctx.body
        .push(format!("{} = arith.constant {:e} : {}", cn, c as f32, d));
    ctx.body
        .push(format!("{} = arith.constant {} : {}", ceps, eps_str, d));

    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}x{}>",
        sq_init, r, c, d
    ));
    ctx.body.push(format!(
        "{} = linalg.mul ins({}, {} : tensor<{}x{}x{}>, tensor<{}x{}x{}>) outs({} : tensor<{}x{}x{}>) -> tensor<{}x{}x{}>",
        sq, src_tensor, src_tensor,
        r, c, d, r, c, d, sq_init, r, c, d, r, c, d
    ));

    ctx.body.push(format!("{} = tensor.empty() : tensor<{}x{}>", red_init, r, d));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : {}) outs({} : tensor<{}x{}>) -> tensor<{}x{}>",
        red_fill, c0, d, red_init, r, d, r, d
    ));
    ctx.body.push(format!(
        "{} = linalg.reduce ins({} : tensor<{}x{}x{}>) outs({} : tensor<{}x{}>) dimensions = [1]",
        red, sq, r, c, d, red_fill, r, d
    ));
    ctx.body.push(format!("  (%in: {}, %acc: {}) {{", d, d));
    ctx.body.push(format!("    %s = arith.addf %in, %acc : {}", d));
    ctx.body.push(format!("    linalg.yield %s : {}", d));
    ctx.body.push("  }".to_string());

    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}>",
        scale_init, r, d
    ));
    ctx.body.push(format!(
        "{} = linalg.generic {{indexing_maps = [affine_map<(d0) -> (d0)>, affine_map<(d0) -> (d0)>], iterator_types = [\"parallel\"]}} ins({} : tensor<{}x{}>) outs({} : tensor<{}x{}>) {{",
        scale, red, r, d, scale_init, r, d
    ));
    ctx.body.push(format!("  ^bb0(%in: {}, %out: {}):", d, d));
    ctx.body.push(format!("    %mean = arith.divf %in, {} : {}", cn, d));
    ctx.body.push(format!("    %var  = arith.addf %mean, {} : {}", ceps, d));
    ctx.body.push(format!("    %rs   = math.rsqrt %var : {}", d));
    ctx.body.push(format!("    linalg.yield %rs : {}", d));
    ctx.body
        .push(format!("}} -> tensor<{}x{}>", r, d));

    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}x{}>",
        out_init, r, c, d
    ));
    ctx.body.push(format!(
        "{} = linalg.generic {{indexing_maps = [affine_map<(d0, d1) -> (d0, d1)>, affine_map<(d0, d1) -> (d0)>, affine_map<(d0, d1) -> (d0, d1)>], iterator_types = [\"parallel\", \"parallel\"]}} ins({}, {} : tensor<{}x{}x{}>, tensor<{}x{}>) outs({} : tensor<{}x{}x{}>) {{",
        out, src_tensor, scale, r, c, d, r, d, out_init, r, c, d
    ));
    ctx.body
        .push(format!("  ^bb0(%xi: {}, %si: {}, %o: {}):", d, d, d));
    ctx.body
        .push(format!("    %y = arith.mulf %xi, %si : {}", d));
    ctx.body.push(format!("    linalg.yield %y : {}", d));
    ctx.body
        .push(format!("}} -> tensor<{}x{}x{}>", r, c, d));

    if let Some(res) = result {
        ctx.record_tensor(&res, &out, (r, c, d));
    }
    Ok(())
}

/// Attention: softmax(Q·K^T / sqrt(D)) · V
///
/// Intrinsic signature: `(dst_ignored, q, k, v, seq_len, head_dim)`
/// Q, K, V are all `tensor<S×D×f32>`. Output is `tensor<S×D×f32>`.
///
/// Emits:
///   %kt     = linalg.transpose(K)                      : (D, S)
///   %scores = linalg.matmul(Q, kt)                     : (S, S)
///   %scaled = linalg.generic (scores * (1/sqrt(D)))    : (S, S)
///   %probs  = linalg.softmax dimension(1)(scaled)      : (S, S)
///   %out    = linalg.matmul(probs, V)                  : (S, D)
fn translate_attention(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if args.len() < 6 {
        return Err(format!(
            "__tile_attention_f32: expected 6 args, got {}",
            args.len()
        ));
    }
    let q_ssa = args[1].trim();
    let k_ssa = args[2].trim();
    let v_ssa = args[3].trim();
    let s = ctx.resolve_const(args[4].trim());
    let d = ctx.resolve_const(args[5].trim());
    let q_tensor = ctx
        .tensor_of(q_ssa)
        .cloned()
        .ok_or_else(|| format!("attention: unknown tensor {}", q_ssa))?;
    let k_tensor = ctx
        .tensor_of(k_ssa)
        .cloned()
        .ok_or_else(|| format!("attention: unknown tensor {}", k_ssa))?;
    let v_tensor = ctx
        .tensor_of(v_ssa)
        .cloned()
        .ok_or_else(|| format!("attention: unknown tensor {}", v_ssa))?;

    // 1. Transpose K: (S, D) -> (D, S)
    let kt_init = ctx.fresh();
    let kt = ctx.fresh();
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}xf32>",
        kt_init, d, s
    ));
    ctx.body.push(format!(
        "{} = linalg.transpose ins({} : tensor<{}x{}xf32>) outs({} : tensor<{}x{}xf32>) permutation = [1, 0]",
        kt, k_tensor, s, d, kt_init, d, s
    ));

    // 2. scores = Q · K^T : (S, D) × (D, S) -> (S, S)
    let c0_scores = ctx.fresh();
    let scores_init = ctx.fresh();
    let scores_fill = ctx.fresh();
    let scores = ctx.fresh();
    ctx.body
        .push(format!("{} = arith.constant 0.000000e+00 : f32", c0_scores));
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}xf32>",
        scores_init, s, s
    ));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : f32) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
        scores_fill, c0_scores, scores_init, s, s, s, s
    ));
    ctx.body.push(format!(
        "{} = linalg.matmul ins({}, {} : tensor<{}x{}xf32>, tensor<{}x{}xf32>) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
        scores, q_tensor, kt, s, d, d, s, scores_fill, s, s, s, s
    ));

    // 3. scaled = scores * (1/sqrt(D))
    let scale_val = 1.0f64 / (d as f64).sqrt();
    let scale_const = ctx.fresh();
    let scaled_init = ctx.fresh();
    let scaled = ctx.fresh();
    ctx.body.push(format!(
        "{} = arith.constant {:e} : f32",
        scale_const, scale_val
    ));
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}xf32>",
        scaled_init, s, s
    ));
    ctx.body.push(format!(
        "{} = linalg.generic {{indexing_maps = [affine_map<(d0, d1) -> (d0, d1)>, affine_map<(d0, d1) -> (d0, d1)>], iterator_types = [\"parallel\", \"parallel\"]}} ins({} : tensor<{}x{}xf32>) outs({} : tensor<{}x{}xf32>) {{",
        scaled, scores, s, s, scaled_init, s, s
    ));
    ctx.body.push("  ^bb0(%in: f32, %out: f32):".to_string());
    ctx.body
        .push(format!("    %y = arith.mulf %in, {} : f32", scale_const));
    ctx.body.push("    linalg.yield %y : f32".to_string());
    ctx.body
        .push(format!("}} -> tensor<{}x{}xf32>", s, s));

    // 4. probs = softmax(scaled) along dim 1
    let probs_init = ctx.fresh();
    let probs = ctx.fresh();
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}xf32>",
        probs_init, s, s
    ));
    ctx.body.push(format!(
        "{} = linalg.softmax dimension(1) ins({} : tensor<{}x{}xf32>) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
        probs, scaled, s, s, probs_init, s, s, s, s
    ));

    // 5. out = probs · V : (S, S) × (S, D) -> (S, D)
    let c0_out = ctx.fresh();
    let out_init = ctx.fresh();
    let out_fill = ctx.fresh();
    let out = ctx.fresh();
    ctx.body
        .push(format!("{} = arith.constant 0.000000e+00 : f32", c0_out));
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}xf32>",
        out_init, s, d
    ));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : f32) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
        out_fill, c0_out, out_init, s, d, s, d
    ));
    ctx.body.push(format!(
        "{} = linalg.matmul ins({}, {} : tensor<{}x{}xf32>, tensor<{}x{}xf32>) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
        out, probs, v_tensor, s, s, s, d, out_fill, s, d, s, d
    ));

    if let Some(res) = result {
        ctx.record_tensor(&res, &out, (s, d, "f32".to_string()));
    }
    Ok(())
}

