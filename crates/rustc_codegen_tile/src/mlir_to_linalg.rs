fn translate_matvec(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    // Signature: (dst_ignored, a, b, rows, cols)
    // a: (rows,cols) matrix ; b: (cols,) vector ; out: (rows,) vector
    if args.len() < 5 {
        return Err(format!(
            "__tile_matvec_f32: expected 5 args, got {}",
            args.len()
        ));
    }
    let a_ssa = args[1].trim();
    let b_ssa = args[2].trim();
    let rows = ctx.resolve_const(args[3].trim());
    let cols = ctx.resolve_const(args[4].trim());
    let a_tensor = ctx
        .tensor_of(a_ssa)
        .cloned()
        .ok_or_else(|| format!("matvec: unknown tensor {}", a_ssa))?;
    let b_tensor = ctx
        .tensor_of(b_ssa)
        .cloned()
        .ok_or_else(|| format!("matvec: unknown tensor {}", b_ssa))?;

    // b is rank-1 in linalg (its tensor handle is the %argN of a 1D load).
    ctx.rank1_tensors.insert(b_tensor.clone());
    if let Some(idx) = b_tensor
        .strip_prefix("%arg")
        .and_then(|s| s.parse::<usize>().ok())
    {
        ctx.rank1_args.insert(idx);
    }

    let c0 = ctx.fresh();
    let empty = ctx.fresh();
    let filled = ctx.fresh();
    let out = ctx.fresh();
    ctx.body
        .push(format!("{} = arith.constant 0.000000e+00 : f32", c0));
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}xf32>",
        empty, rows
    ));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : f32) outs({} : tensor<{}xf32>) -> tensor<{}xf32>",
        filled, c0, empty, rows, rows
    ));
    ctx.body.push(format!(
        "{} = linalg.matvec ins({}, {} : tensor<{}x{}xf32>, tensor<{}xf32>) outs({} : tensor<{}xf32>) -> tensor<{}xf32>",
        out, a_tensor, b_tensor, rows, cols, cols, filled, rows, rows
    ));
    if let Some(r) = result {
        // Represent a rank-1 (rows,) tensor as shape (rows,1,f32) in our ctx,
        // but flag it rank-1 so the final signature/return are printed as 1D.
        ctx.record_tensor(&r, &out, (rows, 1, "f32".to_string()));
        ctx.rank1_tensors.insert(out);
    }
    Ok(())
}

