/// Unary ops that don't have a named linalg op — emitted as `linalg.generic`
/// with a one-line region body. `body` is the single op applied to `%in`
/// producing the yielded value (e.g. `math.rsqrt`, or an expanded sigmoid).
#[derive(Debug, Clone, Copy)]
enum UnaryKind {
    Rsqrt,
    Sigmoid,
    Silu,
    Relu,
}

fn translate_generic_unop(
    kind: UnaryKind,
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if args.len() < 4 {
        return Err(format!(
            "generic unop: expected 4 args, got {}",
            args.len()
        ));
    }
    let src_ssa = args[1].trim();
    let shape = ctx
        .shape_of(src_ssa)
        .ok_or_else(|| format!("generic unop: unknown shape for {}", src_ssa))?;
    let src_tensor = ctx
        .tensor_of(src_ssa)
        .cloned()
        .ok_or_else(|| format!("generic unop: unknown tensor {}", src_ssa))?;

    let empty = ctx.fresh();
    let out = ctx.fresh();
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}x{}>",
        empty, shape.0, shape.1, shape.2
    ));
    ctx.body.push(format!(
        "{} = linalg.generic {{indexing_maps = [affine_map<(d0, d1) -> (d0, d1)>, affine_map<(d0, d1) -> (d0, d1)>], iterator_types = [\"parallel\", \"parallel\"]}} ins({} : tensor<{}x{}x{}>) outs({} : tensor<{}x{}x{}>) {{",
        out, src_tensor, shape.0, shape.1, shape.2,
        empty, shape.0, shape.1, shape.2,
    ));
    ctx.body.push("  ^bb0(%in: f32, %out: f32):".to_string());
    match kind {
        UnaryKind::Rsqrt => {
            ctx.body.push("    %y = math.rsqrt %in : f32".to_string());
        }
        UnaryKind::Sigmoid => {
            // sigmoid(x) = 1 / (1 + exp(-x))
            ctx.body.push("    %nx = arith.negf %in : f32".to_string());
            ctx.body.push("    %e = math.exp %nx : f32".to_string());
            ctx.body
                .push("    %one = arith.constant 1.0 : f32".to_string());
            ctx.body.push("    %d = arith.addf %e, %one : f32".to_string());
            ctx.body.push("    %y = arith.divf %one, %d : f32".to_string());
        }
        UnaryKind::Silu => {
            // silu(x) = x * sigmoid(x) = x / (1 + exp(-x))
            ctx.body.push("    %nx = arith.negf %in : f32".to_string());
            ctx.body.push("    %e = math.exp %nx : f32".to_string());
            ctx.body
                .push("    %one = arith.constant 1.0 : f32".to_string());
            ctx.body.push("    %d = arith.addf %e, %one : f32".to_string());
            ctx.body.push("    %y = arith.divf %in, %d : f32".to_string());
        }
        UnaryKind::Relu => {
            // relu(x) = max(x, 0)
            ctx.body
                .push("    %zero = arith.constant 0.0 : f32".to_string());
            ctx.body
                .push("    %y = arith.maximumf %in, %zero : f32".to_string());
        }
    }
    ctx.body.push("    linalg.yield %y : f32".to_string());
    ctx.body.push(format!(
        "}} -> tensor<{}x{}x{}>",
        shape.0, shape.1, shape.2
    ));

    if let Some(r) = result {
        ctx.record_tensor(&r, &out, shape);
    }
    Ok(())
}

/// `__tile_scale_f32(dst, src, scalar, rows, cols)` →
/// `linalg.generic { arith.mulf %in, %scalar }`.
fn translate_scale(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if args.len() < 5 {
        return Err(format!(
            "__tile_scale_f32: expected 5 args, got {}",
            args.len()
        ));
    }
    let src_ssa = args[1].trim();
    let scalar_ssa = args[2].trim();
    let shape = ctx
        .shape_of(src_ssa)
        .ok_or_else(|| format!("scale: unknown shape for {}", src_ssa))?;
    let src_tensor = ctx
        .tensor_of(src_ssa)
        .cloned()
        .ok_or_else(|| format!("scale: unknown tensor {}", src_ssa))?;

    // The scalar should be a compile-time llvm.mlir.constant. resolve_float
    // returns the literal form (e.g. "2.500000e-01"); we emit it verbatim as
    // an arith.constant. If it wasn't a constant, resolve_float returns the
    // raw SSA name — which will cause mlir-opt to reject the module with a
    // diagnostic, surfacing the mistake at verify time.
    let scale_lit = ctx.resolve_float(scalar_ssa);

    let scale_const = ctx.fresh();
    let empty = ctx.fresh();
    let out = ctx.fresh();
    ctx.body
        .push(format!("{} = arith.constant {} : f32", scale_const, scale_lit));
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}x{}>",
        empty, shape.0, shape.1, shape.2
    ));
    ctx.body.push(format!(
        "{} = linalg.generic {{indexing_maps = [affine_map<(d0, d1) -> (d0, d1)>, affine_map<(d0, d1) -> (d0, d1)>], iterator_types = [\"parallel\", \"parallel\"]}} ins({} : tensor<{}x{}x{}>) outs({} : tensor<{}x{}x{}>) {{",
        out, src_tensor, shape.0, shape.1, shape.2,
        empty, shape.0, shape.1, shape.2,
    ));
    ctx.body.push("  ^bb0(%in: f32, %out: f32):".to_string());
    ctx.body
        .push(format!("    %y = arith.mulf %in, {} : f32", scale_const));
    ctx.body.push("    linalg.yield %y : f32".to_string());
    ctx.body.push(format!(
        "}} -> tensor<{}x{}x{}>",
        shape.0, shape.1, shape.2
    ));

    if let Some(r) = result {
        ctx.record_tensor(&r, &out, shape);
    }
    Ok(())
}

/// `__tile_fill_f32(dst, scalar, rows, cols)` →
/// `tensor.empty + linalg.fill`.
///
/// Unlike most translators, fill has no source tile to draw shape from —
/// we read `rows` and `cols` from the intrinsic's own arguments.
fn translate_fill(
    args: &[String],
    result: Option<String>,
    ctx: &mut LinalgContext,
) -> Result<(), String> {
    if args.len() < 4 {
        return Err(format!(
            "__tile_fill_f32: expected 4 args, got {}",
            args.len()
        ));
    }
    let scalar_ssa = args[1].trim();
    let rows = ctx.resolve_const(args[2].trim());
    let cols = ctx.resolve_const(args[3].trim());
    let scalar_lit = ctx.resolve_float(scalar_ssa);

    let scalar_const = ctx.fresh();
    let empty = ctx.fresh();
    let filled = ctx.fresh();
    ctx.body.push(format!(
        "{} = arith.constant {} : f32",
        scalar_const, scalar_lit
    ));
    ctx.body.push(format!(
        "{} = tensor.empty() : tensor<{}x{}xf32>",
        empty, rows, cols
    ));
    ctx.body.push(format!(
        "{} = linalg.fill ins({} : f32) outs({} : tensor<{}x{}xf32>) -> tensor<{}x{}xf32>",
        filled, scalar_const, empty, rows, cols, rows, cols
    ));

    if let Some(r) = result {
        ctx.record_tensor(&r, &filled, (rows, cols, "f32".to_string()));
    }
    Ok(())
}

