//! MLIR-to-linalg translator for frontend interoperability.
//!
//! Converts merged MLIR modules (LLVM dialect with `__tile_*` intrinsics)
//! into `linalg-on-tensors` MLIR. Unlike the per-vendor backends, this emitter
//! targets the neutral `linalg` dialect — the same form produced by Torch-MLIR,
//! IREE's input format, Mojo MAX, and JAX→stablehlo→linalg.
//!
//! The output is consumable by any MLIR frontend that speaks linalg. This is
//! the *egress* side of the linalg bridge; `linalg_to_ascend_tile.rs` handles
//! ingress (external linalg → ascend_tile).
//!
//! # Shape of the output
//!
//! ```mlir
//! func.func @tile_softmax(%arg0: tensor<1x1024xf32>) -> tensor<1x1024xf32> {
//!   %0 = tensor.empty() : tensor<1x1024xf32>
//!   %1 = linalg.softmax dimension(1) ins(%arg0 : tensor<1x1024xf32>)
//!                                      outs(%0 : tensor<1x1024xf32>) -> tensor<1x1024xf32>
//!   return %1 : tensor<1x1024xf32>
//! }
//! ```
//!
//! # Intrinsic → linalg named op mapping (Phase 1 coverage)
//!
//! | tile_std intrinsic              | linalg op                          |
//! |-----------------------------------|------------------------------------|
//! | `__tile_load_f32`            | implicit (function arg tensor)     |
//! | `__tile_store_f32`           | implicit (function return)         |
//! | `__tile_add_f32`             | `linalg.add`                        |
//! | `__tile_mul_f32`             | `linalg.mul`                        |
//! | `__tile_exp_f32`             | `linalg.exp`                        |
//! | `__tile_matmul_f32`          | `linalg.fill` + `linalg.matmul`     |
//! | `__tile_softmax_f32`         | `linalg.softmax dimension(1)`       |
//! | `__tile_rms_norm_f32`        | `linalg.mul` + `linalg.reduce` + 2× `linalg.generic` |
//! | `__tile_matvec_f32`          | `linalg.fill` + `linalg.matvec`     |
//! | `__tile_transpose_f32`       | `linalg.transpose permutation=[1,0]`|
//! | `__tile_reduce_sum_f32`      | `linalg.fill(0)` + `linalg.reduce addf` |
//! | `__tile_reduce_max_f32`      | `linalg.fill(-inf)` + `linalg.reduce maximumf` |
//! | `__tile_attention_f32`       | `linalg.transpose` + 2× `linalg.matmul` + scale `linalg.generic` + `linalg.softmax` |
//! | `__tile_rsqrt_f32`           | `linalg.generic { math.rsqrt }`     |
//! | `__tile_sigmoid_f32`         | `linalg.generic { 1/(1+exp(-x)) }`  |
//! | `__tile_silu_f32`            | `linalg.generic { x/(1+exp(-x)) }`  |
//! | `__tile_scale_f32`           | `linalg.generic { arith.mulf %in, const }` |
//! | `__tile_clamp_f32`           | `linalg.generic { maximumf → minimumf }`   |
//! | `__tile_causal_mask_f32`     | `linalg.generic { index + cmpi + select -inf }` |
//! | `__tile_cast_f32_f16`        | `linalg.generic { arith.truncf }` (tensor retyped) |
//! | `__tile_cast_f16_f32`        | `linalg.generic { arith.extf }` (tensor retyped)   |
//! | `__tile_cast_f32_bf16`       | `linalg.generic { arith.truncf }` (tensor retyped) |
//! | `__tile_cast_bf16_f32`       | `linalg.generic { arith.extf }` (tensor retyped)   |

use std::collections::{HashMap, HashSet};
use std::fmt::Write;

use crate::mlir_to_pto::{
    extract_call_args, extract_result_ssa, is_builtin_helper, parse_const_arg, parse_module,
    FuncArg, MlirFunc,
};

/// Convert MLIR text (merged module, LLVM dialect) into linalg-on-tensors MLIR.
///
/// Returns the MLIR source string, or an error on parse failure.
pub fn convert_mlir_to_linalg(mlir_text: &str) -> Result<String, String> {
    let module = parse_module(mlir_text)?;

    let mut out = String::with_capacity(4096);
    writeln!(out, "// Generated by tile-rs mlir_to_linalg — DO NOT EDIT").unwrap();
    writeln!(
        out,
        "// Verify: mlir-opt --verify-diagnostics <this_file>"
    )
    .unwrap();
    writeln!(out).unwrap();

    let mut kernel_count = 0;
    for func in &module.functions {
        if func.is_entry && !func.body_lines.is_empty() && !is_builtin_helper(&func.name) {
            generate_func_linalg(func, &mut out)?;
            kernel_count += 1;
        }
    }

    if kernel_count == 0 {
        return Err("No entry-point kernel functions found in MLIR module".into());
    }

    Ok(out)
}

// ---------------------------------------------------------------------------
// Linalg context: tracks SSA → tensor var names and shapes
// ---------------------------------------------------------------------------

struct LinalgContext {
    /// MLIR SSA (e.g. `%5`) → linalg tensor SSA (e.g. `%t0`)
    tensor_vars: HashMap<String, String>,
    /// linalg tensor SSA → (rows, cols, dtype)
    tensor_shapes: HashMap<String, (u32, u32, String)>,
    /// linalg tensor SSA marked as rank-1 (shape printed as `tensor<Nxd>`
    /// with N == rows). Populated by matvec / reduce translators and
    /// propagated to function args / return when a marked tensor reaches
    /// a store or an arg_shape site.
    rank1_tensors: HashSet<String>,
    /// ptr arg index → "this arg is rank-1 on the linalg side"
    rank1_args: HashSet<usize>,
    /// Counter for fresh tensor/intermediate SSAs
    next_tensor: u32,
    /// Integer constants: SSA → u32
    const_map: HashMap<String, u32>,
    /// Float constants: SSA → literal string
    float_const_map: HashMap<String, String>,
    /// Pointer SSA → owning function-arg index (i.e. which `%argN`)
    arg_idx_of_ptr: HashMap<String, usize>,
    /// Per-arg inferred tensor shape, once seen at load/store time
    arg_shape: HashMap<usize, (u32, u32, String)>,
    /// Argument index that is the primary return target (first stored-to arg)
    output_arg: Option<usize>,
    /// Lines for the function body (collected before we know the return shape)
    body: Vec<String>,
    /// Final SSA to return (computed from the last store)
    return_ssa: Option<String>,
}

impl LinalgContext {
    fn new() -> Self {
        Self {
            tensor_vars: HashMap::new(),
            tensor_shapes: HashMap::new(),
            rank1_tensors: HashSet::new(),
            rank1_args: HashSet::new(),
            next_tensor: 0,
            const_map: HashMap::new(),
            float_const_map: HashMap::new(),
            arg_idx_of_ptr: HashMap::new(),
            arg_shape: HashMap::new(),
            output_arg: None,
            body: Vec::new(),
            return_ssa: None,
        }
    }

    /// Format an input/output tensor shape, respecting the rank-1 flag.
    fn fmt_tensor(&self, r: u32, c: u32, d: &str, rank1: bool) -> String {
        if rank1 {
            format!("tensor<{}x{}>", r, d)
        } else {
            format!("tensor<{}x{}x{}>", r, c, d)
        }
    }

    fn fresh(&mut self) -> String {
        let v = format!("%t{}", self.next_tensor);
        self.next_tensor += 1;
        v
    }

    fn resolve_const(&self, s: &str) -> u32 {
        if let Some(&n) = self.const_map.get(s.trim()) {
            return n;
        }
        parse_const_arg(s)
    }

    fn resolve_float(&self, s: &str) -> String {
        let s = s.trim();
        if let Some(v) = self.float_const_map.get(s) {
            return v.clone();
        }
        if let Some(&n) = self.const_map.get(s) {
            return format!("{:.6e}", n as f32);
        }
        s.to_string()
    }

    fn tensor_of(&self, ssa: &str) -> Option<&String> {
        self.tensor_vars.get(ssa.trim())
    }

    fn shape_of(&self, ssa: &str) -> Option<(u32, u32, String)> {
        if let Some(v) = self.tensor_vars.get(ssa.trim()) {
            return self.tensor_shapes.get(v).cloned();
        }
        None
    }

    fn record_tensor(&mut self, src_ssa: &str, tensor_ssa: &str, shape: (u32, u32, String)) {
        self.tensor_vars
            .insert(src_ssa.to_string(), tensor_ssa.to_string());
        self.tensor_shapes.insert(tensor_ssa.to_string(), shape);
    }
}

// ---------------------------------------------------------------------------
// Top-level function generator
// ---------------------------------------------------------------------------

fn generate_func_linalg(func: &MlirFunc, out: &mut String) -> Result<(), String> {
    let mut ctx = LinalgContext::new();

    // Collect GM pointer args; linalg signature will use their inferred tensor shapes.
    let ptr_args: Vec<&FuncArg> = func.args.iter().filter(|a| a.is_gm).collect();
    for (i, arg) in ptr_args.iter().enumerate() {
        ctx.arg_idx_of_ptr.insert(arg.name.clone(), i);
    }

    // Translate body (collects ops into ctx.body, infers arg_shape along the way)
    for line in &func.body_lines {
        translate_line(line, &mut ctx)?;
    }

    // Signature assembly: inputs = args with shape recorded at load sites;
    // output = arg with shape recorded at store site.
    let out_idx = ctx.output_arg.ok_or_else(|| {
        format!(
            "mlir_to_linalg: no output tensor inferred for {} (missing __tile_store?)",
            func.name
        )
    })?;

    // Emit function signature
    write!(out, "func.func @{}(", func.name).unwrap();
    let mut input_args: Vec<(usize, (u32, u32, String))> = Vec::new();
    for (i, _arg) in ptr_args.iter().enumerate() {
        if i == out_idx {
            continue;
        }
        let shape = ctx
            .arg_shape
            .get(&i)
            .cloned()
            .ok_or_else(|| format!("mlir_to_linalg: arg {} never loaded in {}", i, func.name))?;
        input_args.push((i, shape));
    }
    for (k, (i, (r, c, d))) in input_args.iter().enumerate() {
        if k > 0 {
            write!(out, ", ").unwrap();
        }
        let rank1 = ctx.rank1_args.contains(i);
        write!(out, "%arg{}: {}", i, ctx.fmt_tensor(*r, *c, d, rank1)).unwrap();
    }
    let out_shape = ctx
        .arg_shape
        .get(&out_idx)
        .cloned()
        .ok_or_else(|| format!("mlir_to_linalg: output arg {} has no shape", out_idx))?;
    let out_rank1 = ctx.rank1_args.contains(&out_idx);
    writeln!(
        out,
        ") -> {} {{",
        ctx.fmt_tensor(out_shape.0, out_shape.1, &out_shape.2, out_rank1)
    )
    .unwrap();

    for line in &ctx.body {
        writeln!(out, "  {}", line).unwrap();
    }

    let ret = ctx.return_ssa.as_deref().ok_or_else(|| {
        format!("mlir_to_linalg: no return SSA recorded for {}", func.name)
    })?;
    writeln!(
        out,
        "  return {} : {}",
        ret,
        ctx.fmt_tensor(out_shape.0, out_shape.1, &out_shape.2, out_rank1)
    )
    .unwrap();
    writeln!(out, "}}").unwrap();
    writeln!(out).unwrap();
    Ok(())
}

// ---------------------------------------------------------------------------
// Per-line translator
// ---------------------------------------------------------------------------

fn translate_line(line: &str, ctx: &mut LinalgContext) -> Result<(), String> {
    let trimmed = line.trim();
    if trimmed.is_empty()
        || trimmed.starts_with("//")
        || trimmed == "^bb0:"
        || trimmed.starts_with("llvm.return")
    {
        return Ok(());
    }

    // Constants: llvm.mlir.constant
    if let Some(res) = extract_result_ssa(line) {
        if line.contains("llvm.mlir.constant") {
            // Parse `N : i32` or `1.0e-5 : f32` / `2.500000e-01 : f32`
            if let Some(cst) = parse_constant_val(line) {
                match cst {
                    ConstVal::Int(n) => {
                        ctx.const_map.insert(res, n);
                    }
                    ConstVal::Float(s) => {
                        ctx.float_const_map.insert(res, s);
                    }
                }
            }
            return Ok(());
        }
    }

    // llvm.call @__tile_*
    if line.contains("llvm.call @") {
        translate_call(line, ctx)?;
    }

    Ok(())
}

enum ConstVal {
    Int(u32),
    Float(String),
}

fn parse_constant_val(line: &str) -> Option<ConstVal> {
    // `%X = llvm.mlir.constant(<V> : <TY>) : <TY>`
    let start = line.find('(')? + 1;
    let end = line[start..].find(')')? + start;
    let inner = line[start..end].trim();
    // Split on " : "
    let colon = inner.rfind(':')?;
    let v = inner[..colon].trim();
    let ty = inner[colon + 1..].trim();
    if ty == "f32" || ty == "f16" || ty == "bf16" {
        Some(ConstVal::Float(v.to_string()))
    } else {
        v.parse::<u32>().ok().map(ConstVal::Int)
    }
}

fn translate_call(line: &str, ctx: &mut LinalgContext) -> Result<(), String> {
    let callee = {
        let start = match line.find("llvm.call @") {
            Some(s) => s + "llvm.call @".len(),
            None => return Ok(()),
        };
        let rest = &line[start..];
        let end = rest
            .find(|c: char| !c.is_alphanumeric() && c != '_')
            .unwrap_or(rest.len());
        rest[..end].to_string()
    };
    let args = match extract_call_args(line) {
        Some(a) => a,
        None => return Ok(()),
    };
    let result = extract_result_ssa(line);

    match callee.as_str() {
        // ── __tile_load_f32(ptr, rows, cols) → i32 ──
        "__tile_load_f32" => {
            if args.len() < 3 {
                return Err(format!(
                    "__tile_load_f32: expected 3 args, got {}",
                    args.len()
                ));
            }
            let ptr_ssa = args[0].trim();
            let rows = ctx.resolve_const(args[1].trim());
            let cols = ctx.resolve_const(args[2].trim());
            let arg_idx = *ctx
                .arg_idx_of_ptr
                .get(ptr_ssa)
                .ok_or_else(|| format!("__tile_load_f32: unknown ptr {}", ptr_ssa))?;
            ctx.arg_shape
                .insert(arg_idx, (rows, cols, "f32".to_string()));
            if let Some(r) = result {
                let tensor = format!("%arg{}", arg_idx);
                ctx.record_tensor(&r, &tensor, (rows, cols, "f32".to_string()));
            }
            Ok(())
        }

        // ── __tile_store_{f32,f16,bf16,i32}(ptr, tile, rows, cols) ──
        // All store variants share one arm: the `ctx.arg_shape` dtype is
        // taken from the producing tile's recorded dtype (via shape_of),
        // not the intrinsic name, so a single handler is correct for all
        // four. The `_i32` variant is emitted by the ingress for argmax /
        // sample_top_p output paths.
        "__tile_store_f32"
        | "__tile_store_f16"
        | "__tile_store_bf16"
        | "__tile_store_i32" => {
            if args.len() < 4 {
                return Err(format!(
                    "__tile_store_*: expected 4 args, got {}",
                    args.len()
                ));
            }
            let ptr_ssa = args[0].trim();
            let tile_ssa = args[1].trim();
            let rows = ctx.resolve_const(args[2].trim());
            let cols = ctx.resolve_const(args[3].trim());
            let arg_idx = *ctx
                .arg_idx_of_ptr
                .get(ptr_ssa)
                .ok_or_else(|| format!("__tile_store_*: unknown ptr {}", ptr_ssa))?;
            // Use the tile's recorded dtype if known (e.g. i32 for argmax);
            // otherwise default to f32. The intrinsic is named `_f32` for
            // ABI compatibility but the emitter prints whatever dtype the
            // producing op declared.
            let dtype = ctx
                .shape_of(tile_ssa)
                .map(|(_, _, d)| d)
                .unwrap_or_else(|| "f32".to_string());
            ctx.arg_shape.insert(arg_idx, (rows, cols, dtype));
            ctx.output_arg = Some(arg_idx);
            let ret = ctx
                .tensor_of(tile_ssa)
                .cloned()
                .unwrap_or_else(|| tile_ssa.to_string());
            // Propagate rank-1 flag: if the tile we're storing is marked rank-1,
            // the output arg's linalg form must also be rank-1.
            if ctx.rank1_tensors.contains(&ret) {
                ctx.rank1_args.insert(arg_idx);
            }
            ctx.return_ssa = Some(ret);
            Ok(())
        }

        // ── Binary elementwise ──
        "__tile_add_f32" => translate_elementwise_binop("linalg.add", &args, result, ctx),
        "__tile_mul_f32" => translate_elementwise_binop("linalg.mul", &args, result, ctx),
        "__tile_sub_f32" => translate_elementwise_binop("linalg.sub", &args, result, ctx),
        "__tile_div_f32" => translate_elementwise_binop("linalg.div", &args, result, ctx),
        "__tile_max_f32" => translate_elementwise_binop("linalg.max", &args, result, ctx),
        "__tile_min_f32" => translate_elementwise_binop("linalg.min", &args, result, ctx),

        // ── Unary elementwise ──
        "__tile_exp_f32" => translate_elementwise_unop("linalg.exp", &args, result, ctx),
        "__tile_log_f32" => translate_elementwise_unop("linalg.log", &args, result, ctx),
        "__tile_neg_f32" => translate_elementwise_unop("linalg.negf", &args, result, ctx),
        "__tile_tanh_f32" => translate_elementwise_unop("linalg.tanh", &args, result, ctx),
        "__tile_abs_f32" => translate_elementwise_unop("linalg.abs", &args, result, ctx),
        "__tile_sqrt_f32" => translate_elementwise_unop("linalg.sqrt", &args, result, ctx),

        // ── Scalar broadcast fill ──
        "__tile_fill_f32" => translate_fill(&args, result, ctx),

        // ── Unary-via-generic (no named linalg op exists) ──
        "__tile_rsqrt_f32" => {
            translate_generic_unop(UnaryKind::Rsqrt, &args, result, ctx)
        }
        "__tile_sigmoid_f32" => {
            translate_generic_unop(UnaryKind::Sigmoid, &args, result, ctx)
        }
        "__tile_silu_f32" => {
            translate_generic_unop(UnaryKind::Silu, &args, result, ctx)
        }
        "__tile_relu_f32" => {
            translate_generic_unop(UnaryKind::Relu, &args, result, ctx)
        }

        // ── Scalar × tile ──
        "__tile_scale_f32" => translate_scale(&args, result, ctx),

        // ── Clamp(min,max) ──
        "__tile_clamp_f32" => translate_clamp(&args, result, ctx),

        // ── Causal mask (upper triangle → -inf) ──
        "__tile_causal_mask_f32" => translate_causal_mask(&args, result, ctx),

        // ── Opaque tile ops (carried as linalg.generic + attribute) ──
        // These ops have no natural decomposition into standard linalg/arith —
        // they operate over lookup tables (embedding), position indices (rope),
        // or externally-managed state (kv_cache_update). The bridge preserves
        // them as `linalg.generic` with an `__tile_op` attribute that
        // downstream consumers (vendor backends) can recognize and re-emit.
        "__tile_rope_f32" => translate_rope(&args, result, ctx),
        "__tile_rope_inplace_f32" => {
            translate_opaque_unary("rope_inplace_f32", &args, result, ctx)
        }
        "__tile_rope_prefill_f32" => {
            translate_opaque_unary("rope_prefill_f32", &args, result, ctx)
        }
        "__tile_kv_cache_update_f32" => {
            translate_opaque_unary("kv_cache_update_f32", &args, result, ctx)
        }
        "__tile_kv_cache_update_prefill_f32" => {
            translate_opaque_unary("kv_cache_update_prefill_f32", &args, result, ctx)
        }

        // ── Phase 4: INT8 quantization ─────────────────────────────
        // quantize_f32_i8(dst, src, scale, rows, cols)
        // dequantize_i8_f32(dst, src, scale, rows, cols)
        //
        // Emitted as opaque generics on f32 tensors with a `scale = <f32>`
        // extra attr. We don't encode the i8 dtype change in the linalg
        // tensor type — the `__tile_op` carrier is about IR
        // pass-through, not vendor semantics. Downstream consumers that
        // care (e.g. a quant-aware pass) read the attribute.
        "__tile_quantize_f32_i8" => {
            translate_scaled_opaque("quantize_f32_i8", &args, result, ctx)
        }
        "__tile_dequantize_i8_f32" => {
            translate_scaled_opaque("dequantize_i8_f32", &args, result, ctx)
        }

        // ── Opaque multi-source (same shape out as each src) ──
        // attention_decode_f32(dst, q, k, v, rows, cols)
        "__tile_attention_decode_f32" => translate_opaque_multi(
            "attention_decode_f32",
            &args,
            &[1, 2, 3],
            result,
            ctx,
        ),
        "__tile_attention_prefill_f32" => translate_opaque_multi(
            "attention_prefill_f32",
            &args,
            &[1, 2, 3],
            result,
            ctx,
        ),

        // ── Dtype casts ──
        "__tile_cast_f32_f16" => {
            translate_cast(&args, result, ctx, "f32", "f16", "arith.truncf")
        }
        "__tile_cast_f16_f32" => {
            translate_cast(&args, result, ctx, "f16", "f32", "arith.extf")
        }
        "__tile_cast_f32_bf16" => {
            translate_cast(&args, result, ctx, "f32", "bf16", "arith.truncf")
        }
        "__tile_cast_bf16_f32" => {
            translate_cast(&args, result, ctx, "bf16", "f32", "arith.extf")
        }

        // ── Named: softmax ──
        "__tile_softmax_f32" => translate_softmax(&args, result, ctx),

        // ── Named: matmul ──
        "__tile_matmul_f32" => translate_matmul(&args, result, ctx),

        // ── Named: matvec ──
        "__tile_matvec_f32" => translate_matvec(&args, result, ctx),

        // ── Named: transpose ──
        "__tile_transpose_f32" => translate_transpose(&args, result, ctx),

        // ── Reductions (row-wise, dim=1) ──
        "__tile_reduce_sum_f32" => {
            translate_row_reduce(&args, result, ctx, ReduceKind::Sum)
        }
        "__tile_reduce_max_f32" => {
            translate_row_reduce(&args, result, ctx, ReduceKind::Max)
        }
        "__tile_absmax_f32" => {
            translate_row_reduce(&args, result, ctx, ReduceKind::AbsMax)
        }

        // ── Phase 6: speculative-decoding primitives ──
        // argmax_f32(dst, src, rows, cols) → tensor<Rxi32> row-wise argmax
        "__tile_argmax_f32" => translate_row_argmax(&args, result, ctx, &[]),

        // sample_top_p_f32(dst, logits, temperature, top_p, rng_seed, rows, cols)
        //   → tensor<Rxi32> sampled token indices per row.
        //
        // The op is a compound (softmax → cumsum → threshold → rng draw)
        // that can't be faithfully decomposed into linalg primitives
        // without a PRNG primitive. We emit it with the argmax-shaped
        // dual-output generic (running max+idx body) + an
        // `__tile_op = "sample_top_p_f32"` attribute carrying the
        // three hyperparameters. Downstream quant/sampling-aware passes
        // read the attribute; plain linalg consumers see a valid generic
        // with a sane identity body and can still lower / bufferize.
        "__tile_sample_top_p_f32" => {
            // ABI: (dst, logits, temperature, top_p, rng_seed, rows, cols).
            // translate_row_argmax expects args = (_dst, src, rows, cols);
            // synthesize that slice so shapes resolve correctly.
            if args.len() < 7 {
                return Err(format!(
                    "__tile_sample_top_p_f32: expected 7 args, got {}",
                    args.len()
                ));
            }
            let temperature = ctx.resolve_float(args[2].trim());
            let top_p = ctx.resolve_float(args[3].trim());
            let rng_seed = ctx.resolve_const(args[4].trim());
            let shaped_args = vec![
                args[0].clone(),
                args[1].clone(),
                args[5].clone(),
                args[6].clone(),
            ];
            translate_row_argmax(
                &shaped_args,
                result,
                ctx,
                &[
                    ("__tile_op", "\"sample_top_p_f32\"".to_string()),
                    ("temperature", format!("{} : f32", temperature)),
                    ("top_p", format!("{} : f32", top_p)),
                    ("rng_seed", format!("{} : i32", rng_seed)),
                ],
            )
        }

        // ── RMSNorm (decomposed) ──
        "__tile_rms_norm_f32" => translate_rms_norm(&args, result, ctx),

        // ── Attention (decomposed): softmax(Q·K^T / sqrt(D)) · V ──
        "__tile_attention_f32" => translate_attention(&args, result, ctx),

        // Unknown intrinsic: pass through as an error so we catch coverage gaps.
        _ => Err(format!(
            "mlir_to_linalg: unsupported intrinsic {} (Phase 1 scope)",
            callee
        )),
    }
}

