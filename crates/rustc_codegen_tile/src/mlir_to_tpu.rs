    // -----------------------------------------------------------------------
    // Error-path coverage: the per-op `if args.len() < N { return Err(...) }`
    // arity guards in translate_call and in the shared helpers
    // (translate_binop=5, translate_binop_fn=5, translate_unary=4,
    // translate_reduce=4, translate_cast=4). Each call passes FEWER args than
    // the op requires, so the guard fires and convert_mlir_to_tpu returns Err.
    //
    // NOTE: like NKI, TPU's source-operand lookups use
    // `get_tile_var(ssa).unwrap_or(ssa)`, so an undefined source SSA does NOT
    // error — only the arity guards are reachable error arms.
    // -----------------------------------------------------------------------

    /// Wrap one too-short intrinsic call in entry-func boilerplate.
    macro_rules! ghost_tpu {
        ($call:expr) => {
            format!(
                "module {{\n  \
                 llvm.func @k(%arg0: !llvm.ptr<1>, %arg1: !llvm.ptr<1>) attributes {{hacc.entry}} {{\n    \
                 {}\n    \
                 llvm.return\n  }}\n}}\n",
                $call
            )
        };
    }

